class Pokemon {
  constructor(id, name, hp, atk, def, catk, cdef, speed, type1, type2) {
    this.id = id;
    this.name = name;
    this.hp = hp;
    this.atk = atk;
    this.def = def;
    this.catk = catk;
    this.cdef = cdef;
    this.speed = speed;
    this.type1 = type1;
    this.type2 = type2;
  }
}

export default Pokemon;
